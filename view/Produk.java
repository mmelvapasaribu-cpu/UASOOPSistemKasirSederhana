private void isiForm() {

    int row = TableProduk.getSelectedRow();

    if (row == -1)
        return;

    idProdukTerpilih = Integer.parseInt(
            modelTabel.getValueAt(row,0).toString());

    InputKodeProduk.setText(modelTabel.getValueAt(row,1).toString());

    InputNamaProduk.setText(modelTabel.getValueAt(row,2).toString());

    InputHarga.setText(
            modelTabel.getValueAt(row,3)
                    .toString()
                    .replaceAll("[^0-9]", "")
    );

    InputStok.setText(modelTabel.getValueAt(row,4).toString());

    InputKategori.setText(modelTabel.getValueAt(row,5).toString());

}

private void muatDataProduk() {

    try {

        tampilkanKeTabel(controller.semuaProduk());

    } catch (Exception ex) {

        JOptionPane.showMessageDialog(this,
                ex.getMessage());

    }

}

    private void bersihkanForm() {
    idProdukTerpilih = -1;

    InputKodeProduk.setText("");

    InputNamaProduk.setText("");

    InputHarga.setText("");

    InputStok.setText("");

    InputKategori.setText("");

    TableProduk.clearSelection();
    
    }

