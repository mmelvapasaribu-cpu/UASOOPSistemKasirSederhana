    private void ButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEditActionPerformed
if(idProdukTerpilih==-1){

        JOptionPane.showMessageDialog(this,
                "Pilih data terlebih dahulu.");

        return;

    }

    try{

        controller.perbaruiProduk(

                idProdukTerpilih,

                InputKodeProduk.getText(),

                InputNamaProduk.getText(),

                Validator.parseDouble(InputHarga.getText(),"Harga"),

                Validator.parseInt(InputStok.getText(),"Stok"),

                InputKategori.getText()

        );

        JOptionPane.showMessageDialog(this,
                "Produk berhasil diubah.");

        bersihkanForm();

        muatDataProduk();

    }

    catch(Exception ex){

        JOptionPane.showMessageDialog(this,
                ex.getMessage());

    }
        // TODO add your handling code here:
    }//GEN-LAST:event_ButtonEditActionPerformed

    private void ButtonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonHapusActionPerformed
if(idProdukTerpilih==-1){

        JOptionPane.showMessageDialog(this,
                "Pilih data terlebih dahulu.");

        return;

    }

    int pilih = JOptionPane.showConfirmDialog(

            this,

            "Yakin ingin menghapus produk?",

            "Konfirmasi",

            JOptionPane.YES_NO_OPTION

    );

    if(pilih==JOptionPane.YES_OPTION){

        try{

            controller.hapusProduk(idProdukTerpilih);

            JOptionPane.showMessageDialog(this,
                    "Produk berhasil dihapus.");

            bersihkanForm();

            muatDataProduk();

        }

        catch(Exception ex){

            JOptionPane.showMessageDialog(this,
                    ex.getMessage());

        }

    }
        // TODO add your handling code here:
    }//GEN-LAST:event_ButtonHapusActionPerformed

