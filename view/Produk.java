private void tampilkanKeTabel(List<model.Produk> daftarProduk) {

    modelTabel.setRowCount(0);

    for (model.Produk p : daftarProduk) {

        modelTabel.addRow(new Object[]{

            p.getIdProduk(),
            p.getKodeProduk(),
            p.getNamaProduk(),
            FormatRupiah.format(p.getHarga()),
            p.getStok(),
            p.getKategori()

        });

    }

}


    

